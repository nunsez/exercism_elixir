defmodule BreadAndPotions do
  @moduledoc """
  You're developing your own role-playing video game. In your game, there are
  _characters_ and _items_. One of the many actions that you can do with an item is
  to make a character eat it.

  Not all items are edible, and not all edible items have the same effects on the
  character. Some items, when eaten, turn into a different item (e.g. if you eat
  an apple, you are left with an apple core).

  To allow for all that flexibility, you decided to create an `Edible` protocol
  that some of the items can implement.

  Tasks:

  1. Define edibility
  2. Make loaves of bread edible
  3. Make mana potions edible
  4. Make poisons edible
  """
end
